public class TypeConversion {
    public static void main(String[] args) {
        // Type casting explicitly converts a value of one type to another type.
        // To type cast precede an expression with (type) to convert the expression's value to the indicated type

        // When type casting make sure to type cast the correct part of the expression
        System.out.println( (double)(15 / 4) );
        System.out.println( 15 / (double)4 );
    }
}
